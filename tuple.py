# tuple of strings
my_data = ("hi", "hello", "bye")
print(my_data)

# tuple of int, float, string
my_data2 = (1, 2.8, "Hello World")
print(my_data2)

# tuple of string and list
my_data3 = ("Book", [1, 2, 3])
print(my_data3)

# tuples inside another tuple
# nested tuple
my_data4 = ((2, 3, 4), (1, 2, "hi"))
print(my_data4)

"""Sequences"""

a="Hello"
print(a[:3])

a=[0, 1, 2, 3]
print(a[:2])

a=4, 5, 6, 4
print(a[:2])

a=range(10)
print(a[:4])
